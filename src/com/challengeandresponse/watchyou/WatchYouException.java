package com.challengeandresponse.watchyou;

public class WatchYouException extends Exception {
	private static final long serialVersionUID = 1L;
	
	public WatchYouException() {
		super();
	}

	public WatchYouException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public WatchYouException(String arg0) {
		super(arg0);
	}

	public WatchYouException(Throwable arg0) {
		super(arg0);
	}
	

}
